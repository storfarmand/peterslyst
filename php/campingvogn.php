    <h2 class="col-lg-10 col-md-10 col-sm-10 col-xs-10">Udlejning af campingvogne</h2>
    <article class="col-lg-5 col-md-5 col-sm-5 col-xs-10">
        <p>Campingferie er en fantastisk ferieform med gode muligheder for afslapning, og sjov for børnene. Vi tager selv gerne afsted når muligheden byder, men i de uger hvor vognene står ubrugte hen lejer vi dem ud i stedet. </p>
        <p>Vores campingvogne er naturligvis rengjort og fuldt udstyret, og skulle du ønske det tilbyder vi at hjælpe dig med opstilling af vogn og fortelt. </p>
    </article>
    <article class="col-lg-5 col-md-5 col-sm-5 col-xs-10"> <strong>BETINGELSER</strong>
        <p>Nedenstående betingelser er fælles for begge vogne: </p>
        <ul>
            <li>Der ligges ud over lejen, et depositum på 2000,- under lejeperioden.</li>
            <li>Vognen afhentes/afleveres ved udlejer i Hasselager, Århus.</li>
            <li>Evt. Opstilling af vogn med telt, kan tilkøbes.</li>
            <li>Lejes ikke ud til festival</li>
        </ul>
    </article>
