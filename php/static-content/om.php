            <h2 class="col-lg-10 col-md-10 col-sm-10 col-xs-10">Lidt om os og vores Gårdbutik</h3>
            <article class="col-lg-3 col-md-3 col-sm-3 col-xs-10">
                <img class="hidden-md hidden-lg" src="gfx/om/palle.jpg" alt="Palle Aagaard Sørensen - ejer af Peterslyst">
                <p>Jeg er uddannet landmand, og har af den vej interessen for at processen "fra jord til bord" skal være forsvarlig, og udmunde i råvarer af en god dansk kvalitet. Jeg kan selv godt lide at vide hvor maden kommer fra, når jeg skal spise det.
				</p>
				<p>
				Med Peterslyst gårdbutik vil vi være med til at øge udbuddet af lokale grøntsager. Vi vil gerne levere så friske varer som muligt, direkte fra marken til forbrugeren. 
				</p>
            </article>
            <article class="col-lg-3 col-md-3 col-sm-3 col-xs-10">
                <img class="hidden-md hidden-lg" src="gfx/om/jordbaer-paa-peterslyst.jpg" alt="Jordbær på marken">
                <p>Vi har valgt som opstart at fokusere på kartofler, gulerødder, ærter, jordbær og græskar. Vi arbejder med sorter der både opfylder vores krav til kvalitet, og samtidig giver den gode danske smag. </p>
				<p>
				I gårdbutikken har vi årsidernes grøntsager. Det kan derfor variere med vejret fra år til år, hvornår de enkelte sorter er til salg i gårdbutikken. I løbet af sommer sæsonen er der mulighed for selv pluk af ærter.</p>
            </article>
            <article class="col-lg-3 col-md-3 col-sm-3 col-xs-10">
                <img class="hidden-md hidden-lg" src="gfx/om/aerter-paa-peterslyst.jpg" alt="Ærter på marken">
                <p>På de 14 ha der er dyrkbare dyrker vi grønsager, bær og forskellige kornafgrøder. Jorden er blandet sand- og muld jord, - en helt perfekt blanding til dyrkning af grønsager. </p>
				<p>
				En lille del af jorden har tidligere været grusgrav og er derfor ikke dyrkbar. Tilgengæld har det resulteret i et pragtfuldt naturområde med et rigt dyreliv, der ligger perfekt placeret ned til Århus Å.
				</p>
            </article>
