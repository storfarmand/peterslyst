  VORES UDVALGTE GRØNSAGS- & FRUGT SORTER
  </h3>
  <article class="col-lg-5 col-md-5 col-sm-5 col-xs-10"> Vores grønsager og bær høstes hver morgen, og sælges samme dag i gårdbutikken. Når ærterne er klar, er du velkommen til at tage familien med til en hyggelig pluk selv dag.
    Ærterne sælges naturligvis også i poser i gårdbutikken. </article>
  <article class="col-lg-5 col-md-5 col-sm-5 col-xs-10"> Med til Peterslyst hører der 17 ha, hvoraf de 14 er blandet sand- og muld jord der kan dyrkes. 
    På dem dyrker vi grønsager, bær og forskellige kornafgrøder. De sorter vi dyrker har vi valgt ud fra bla. smag, udbytte og modtagelighed overfor sygdomme. </article>
