        <h2>UTRILLO ÆRTER</h2>
        <p>Vi har valgt Utrillo ærter fordi det er en saftig, sød og smagsfuld ært. Vi sår vores ærter over flere uger, på den måde sikrer vi os at ærterne bliver spiseklare over tid, og ikke bliver for store og grove. Vi har friske og lækre ærter gennem hele juli.</p>
        <p>Gør det til et hyggeligt indslag med familien i sommer-ferien, tag hele familien med i marken, og pluk ærter. Tag kaffen og et tæppe med, og nyd den mellem duften af bær og fuglesang.</p>
        <p>Ærter har et højt indhold af proteiner og fibre, og næsten ingen kalorier. Ærter har et højt indhold af B- og C-vitaminer og er lækre både friske og som frosne til senere brug.</p>
        <p>Vi sprøjter naturligvis ikke vores ærter, så du kan bruge de friske saftige bælge direkte i salaten. Nybælgede sprøde ærter er en af sommerens danske delikatesser!</p>
